package as.model;

import java.util.Date;

public class Stuarrive {
	private int id;
	private String cardID;
	private String name;
	private Date marrive;
	private Date mleave;
	private Date aarrive;
	private Date aleave;
	private Date adate;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCardID() {
		return cardID;
	}
	public void setCardID(String cardID) {
		this.cardID = cardID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Date getMarrive() {
		return marrive;
	}
	public void setMarrive(Date marrive) {
		this.marrive = marrive;
	}
	public Date getMleave() {
		return mleave;
	}
	public void setMleave(Date mleave) {
		this.mleave = mleave;
	}
	public Date getAarrive() {
		return aarrive;
	}
	public void setAarrive(Date aarrive) {
		this.aarrive = aarrive;
	}
	public Date getAleave() {
		return aleave;
	}
	public void setAleave(Date aleave) {
		this.aleave = aleave;
	}
	public Date getAdate() {
		return adate;
	}
	public void setAdate(Date adate) {
		this.adate = adate;
	}

}
